"""Analyze experiment results from CSV files.

Inputs:
    CSV files generated by run_experiment.py

Outputs:
    results/summary.csv
    reports/qualitative_examples.md
"""

import argparse
from pathlib import Path

import pandas as pd

from config_utils import load_yaml


def contains_pattern(text: str, patterns: list[str]) -> bool:
    """Check whether any evaluation pattern appears in the text."""
    text = str(text).lower()
    return any(pattern.lower() in text for pattern in patterns)


def estimate_attack_success(row: pd.Series, success_patterns: list[str]) -> bool:
    """Estimate whether an attack succeeded.

    If a prompt or output patch blocked the attack, we count it as not successful.
    Otherwise, we inspect the response for configured attack-success patterns.
    """
    if row.get("blocked_by_input", False) or row.get("blocked_by_output", False):
        return False

    return contains_pattern(row.get("response", ""), success_patterns)


def summarize_file(csv_file: Path, success_patterns: list[str]) -> dict:
    """Calculate quantitative metrics for one experiment CSV file."""
    df = pd.read_csv(csv_file)

    # Estimate attack success for each response.
    df["attack_success"] = df.apply(
        lambda row: estimate_attack_success(row, success_patterns),
        axis=1,
    )

    total = len(df)
    successes = int(df["attack_success"].sum())

    return {
        "file": str(csv_file),
        "mode": df["mode"].iloc[0] if total > 0 else "unknown",
        "total_prompts": total,
        "successful_attacks": successes,
        "attack_success_rate": successes / total if total > 0 else 0,
        "blocked_by_input": int(df["blocked_by_input"].sum()),
        "blocked_by_output": int(df["blocked_by_output"].sum()),
    }


def write_qualitative_examples(
    csv_files: list[Path],
    output_file: Path,
    success_patterns: list[str],
):
    """Write selected qualitative examples to a Markdown file."""
    lines = ["# Qualitative Examples", ""]

    for csv_file in csv_files:
        df = pd.read_csv(csv_file)
        mode = df["mode"].iloc[0] if len(df) else "unknown"

        df["attack_success"] = df.apply(
            lambda row: estimate_attack_success(row, success_patterns),
            axis=1,
        )

        lines.append(f"## Mode: {mode}")
        lines.append("")

        # Prefer showing one successful attack if available.
        successful_rows = df[df["attack_success"] == True]
        if len(successful_rows) > 0:
            row = successful_rows.iloc[0]
            lines.append("### Example: Estimated successful attack")
        elif len(df) > 0:
            row = df.iloc[0]
            lines.append("### Example: First available response")
        else:
            continue

        lines.append("")
        lines.append("**Prompt:**")
        lines.append("")
        lines.append(f"> {row['original_prompt']}")
        lines.append("")
        lines.append("**Response:**")
        lines.append("")
        lines.append(f"> {row['response']}")
        lines.append("")

    output_file.parent.mkdir(parents=True, exist_ok=True)
    output_file.write_text("\n".join(lines), encoding="utf-8")


def main():
    """Parse arguments and run the analysis."""
    parser = argparse.ArgumentParser(
        description="Analyze LLM security experiment CSV files."
    )

    parser.add_argument(
        "--csv_files",
        nargs="+",
        required=True,
        help="Experiment CSV files to analyze.",
    )

    parser.add_argument(
        "--summary_file",
        default="results/summary.csv",
        help="Where to save the quantitative summary CSV.",
    )

    parser.add_argument(
        "--examples_file",
        default="reports/qualitative_examples.md",
        help="Where to save qualitative examples.",
    )

    args = parser.parse_args()

    # Load evaluation patterns from config.yaml.
    config = load_yaml("configs/config.yaml")
    success_patterns = config["evaluation"]["attack_success_patterns"]

    csv_files = [Path(path) for path in args.csv_files]

    summaries = [
        summarize_file(csv_file, success_patterns)
        for csv_file in csv_files
    ]

    summary_df = pd.DataFrame(summaries)

    # Calculate relative ASR reduction compared with baseline.
    if "baseline" in summary_df["mode"].values:
        baseline_asr = float(
            summary_df.loc[
                summary_df["mode"] == "baseline",
                "attack_success_rate",
            ].iloc[0]
        )

        if baseline_asr > 0:
            summary_df["relative_asr_reduction"] = (
                baseline_asr - summary_df["attack_success_rate"]
            ) / baseline_asr
        else:
            summary_df["relative_asr_reduction"] = 0.0

    Path(args.summary_file).parent.mkdir(parents=True, exist_ok=True)
    summary_df.to_csv(args.summary_file, index=False)

    write_qualitative_examples(
        csv_files,
        Path(args.examples_file),
        success_patterns,
    )

    print(f"Saved quantitative summary to {args.summary_file}")
    print(f"Saved qualitative examples to {args.examples_file}")
    print(summary_df)


if __name__ == "__main__":
    main()